 package looping;

public class Nested_for {

	public static void main(String[] args) {
		
		     
		  
				for(int i = 1;i <= 10 ; i++) {			
					
					System.out.println(i);
				
				System.out.println("-------------------");
				
				for(int a = 20;a < 30;a++) {
				
				System.out.println(a);
				}
				}	
		

	}

}
